<h2>About</h2>
<p>
	This project (and its tutorial) showcases the following features
	of SvelteKit:
</p>
<ul>
	<li>fixed and dynamic routes</li>
	<li>layouts</li>
	<li>Svelte features:</li>
	<ul>
		<li>components</li>
		<li>props</li>
		<li>scoped styles</li>
		<li>reactive variables</li>
		<li>loops</li>
		<li>&lt;svelte:head&gt;</li>
		<li>injecting html</li>
	</ul>
	<li>page endpoints</li>
	<li>server-side rendering</li>
	<li>static site generation</li>
	<li>disabling JavaScript client-side still works</li>
	<li>the $page store</li>
	<li>prefetching pages</li>
	<li>deployment to Netlify</li>
</ul>
<p>
	The blog posts are stored as <a
		href="https://www.markdownguide.org"
		target="_blank">markdown</a
	>
	files on the server and are rendered as html pages using
	<a href="https://www.npmjs.com/package/marked" target="_blank"
		><tt>marked</tt></a
	>. The metadata from the markdown files are extracted with
	<a
		href="https://www.npmjs.com/package/front-matter"
		target="_blank"><tt>front-matter</tt></a
	>.
</p>
